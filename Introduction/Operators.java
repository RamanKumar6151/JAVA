package Introduction;

public class Operators {
    public static void main(String[] args) {
        double a=1;
        double b=2;
        System.out.println(a+b);
        System.out.println(b-a);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
